# Se crea una lista anidada llamada 'lista_anidada'
# Contiene diferentes tipos de datos y una sublista en la posición 2
lista_anidada = [
    15,                     # Posición 0: número entero
    True,                   # Posición 1: valor booleano
    [25.5, 57.9, 30.6],     # Posición 2: sublista con tres números flotantes
    False                   # Posición 3: valor booleano
]

# Se imprime la lista completa por pantalla
print(lista_anidada)